n = len(net)
count = 0
components = [-1 for i in range(n)]
seen_nodes = [False for i in range(n)]

def FindComp():
  global count
  count = 0
  for i in range(n):
    if not seen_nodes[i]:
      count += 1
      depth_traversal(i, count)
  return count, components

def depth_traversal(start_node, comp_id):
  seen_nodes[start_node] = True
  components[start_node] = comp_id
  for follower in range(n):
    if net[start_node][follower] == 1 and not seen_nodes[follower]:
      depth_traversal(follower, comp_id)

